def main():
    set1 = {1, 2, 3, 4, 5}
    set2 = {4, 5, 6, 7, 8}

    # Intersection of two sets
    intersection = set1.intersection(set2)
    print("Intersection of set1 and set2:", intersection)

    # Union of two sets
    union = set1.union(set2)
    print("Union of set1 and set2:", union)

if __name__ == "__main__":
    main()
