def find_sum_of_elements(lst):
    # Using the built-in sum() function to calculate the sum of elements in the list
    total_sum = sum(lst)
    return total_sum

# Example usage:
my_list = [1, 2, 3, 4, 5]
result = find_sum_of_elements(my_list)

print(f"The sum of elements in the list {my_list} is: {result}")
